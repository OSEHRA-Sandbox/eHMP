package us.vistacore.ehmp.model.demographics;

public class PatientEthnicity {

    private Long id;
    private String code;
    private String name;
    private String vuid;

    public Long getId() {
        return id;
    }

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    public String getVuid() {
        return vuid;
    }
}
